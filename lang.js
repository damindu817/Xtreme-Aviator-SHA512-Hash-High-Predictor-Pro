// Language management functions
let currentLanguage = localStorage.getItem('language') || 'en';

function setLanguage(lang) {
  currentLanguage = lang;
  localStorage.setItem('language', lang);
  applyTranslations();
}

function applyTranslations() {
  const langData = translations[currentLanguage];
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (langData[key]) {
      element.textContent = langData[key];
    }
  });
  
  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    const key = element.getAttribute('data-i18n-placeholder');
    if (langData[key]) {
      element.setAttribute('placeholder', langData[key]);
    }
  });
  
  // Update button values
  document.querySelectorAll('[data-i18n-value]').forEach(element => {
    const key = element.getAttribute('data-i18n-value');
    if (langData[key]) {
      element.value = langData[key];
    }
  });
  
  // Update title
  document.title = langData['appTitle'] || "XTREME AVIATOR SHA512 HASH";
}

// Initialize translations on load
document.addEventListener('DOMContentLoaded', function() {
  applyTranslations();
  
  // Set active language button
  document.querySelectorAll('.language-option').forEach(option => {
    option.classList.remove('active');
    if (option.getAttribute('onclick').includes(`'${currentLanguage}'`)) {
      option.classList.add('active');
    }
  });
});

// Helper function to translate dynamic content
function translate(key, params = {}) {
  const langData = translations[currentLanguage] || translations['en'];
  let text = langData[key] || key;
  
  // Replace placeholders
  for (const [param, value] of Object.entries(params)) {
    text = text.replace(`{${param}}`, value);
  }
  
  return text;
}